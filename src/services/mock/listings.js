export function fetchListings(filters = {}) {
  let result = [...listings];
  if (filters.city) result = result.filter((l) => l.city === filters.city);
  if (filters.type) result = result.filter((l) => l.type === filters.type);
  if (filters.minRent) result = result.filter((l) => l.rent >= Number(filters.minRent));
  if (filters.maxRent) result = result.filter((l) => l.rent <= Number(filters.maxRent));
  if (filters.beds) result = result.filter((l) => l.beds === Number(filters.beds));
  if (filters.minSize) result = result.filter((l) => l.size >= Number(filters.minSize));
  if (filters.maxSize) result = result.filter((l) => l.size <= Number(filters.maxSize));
  if (filters.portion) result = result.filter((l) => l.portion === filters.portion);
  if (filters.residentialType) result = result.filter((l) => l.residentialType === filters.residentialType);
  if (filters.verified) result = result.filter((l) => l.status === "Verified");
  return mockDelay(result, 700);
}

export function fetchListingById(id) {
  return mockDelay(listings.find((l) => l.id === id), 500);
}
